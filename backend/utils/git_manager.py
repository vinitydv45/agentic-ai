def get_github_owner() -> str:
    """Get configured GitHub owner from settings."""
    return settings.github_owner


def get_github_default_branch() -> str:
    """Get configured default branch from settings."""
    return settings.github_default_branch


def is_github_enabled() -> bool:
    """Check if GitHub integration is enabled."""
    return bool(settings.effective_github_token and settings.auto_create_repo)


def get_repo_creation_params(
    project_name: str,
    description: str = "",
    private: bool = True
) -> Dict[str, Any]:
    """
    Generate parameters for GitHub MCP create_repository tool.
    
    Args:
        project_name: Name of the repository
        description: Repository description
        private: Whether the repo should be private
        
    Returns:
        Dictionary of parameters for MCP tool
    """
    params = {
        "name": project_name,
        "description": description or f"React project generated from Figma design",
        "private": private,
        "autoInit": False,
    }
    
    if settings.github_owner:
        params["organization"] = settings.github_owner
    
    return params


def get_push_files_params(
    repo: str,
    files: List[Dict[str, str]],
    branch: str = "main",
    commit_message: str = "feat(figma): Initial project from Figma design"
) -> Dict[str, Any]:
    """
    Generate parameters for GitHub MCP push_files tool.
    
    Args:
        repo: Repository name
        files: List of file objects with 'path' and 'content'
        branch: Branch to push to
        commit_message: Commit message
        
    Returns:
        Dictionary of parameters for MCP tool
    """
    return {
        "owner": settings.github_owner,
        "repo": repo,
        "branch": branch,
        "files": files,
        "message": commit_message,
    }


def get_pr_creation_params(
    repo: str,
    title: str,
    body: str,
    head: str,
    base: str = "main",
    draft: bool = False
) -> Dict[str, Any]:
    """
    Generate parameters for GitHub MCP create_pull_request tool.
    
    Args:
        repo: Repository name
        title: PR title
        body: PR body/description
        head: Head branch (source)
        base: Base branch (target)
        draft: Whether to create as draft
        
    Returns:
        Dictionary of parameters for MCP tool
    """
    return {
        "owner": settings.github_owner,
        "repo": repo,
        "title": title,
        "body": body,
        "head": head,
        "base": base,
        "draft": draft,
    }
